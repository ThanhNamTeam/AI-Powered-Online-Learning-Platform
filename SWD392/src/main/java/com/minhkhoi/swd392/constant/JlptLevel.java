package com.minhkhoi.swd392.constant;

/**
 * Cấp độ JLPT (Japanese Language Proficiency Test).
 * N5: Sơ cấp → N1: Cao cấp.
 * Thứ tự từ cơ bản đến nâng cao.
 */
public enum JlptLevel {
    N5,   // Sơ cấp 1 - Hiragana, Katakana, ~800 từ vựng
    N4,   // Sơ cấp 2 - ~1500 từ vựng
    N3,   // Trung cấp - ~3750 từ vựng
    N2,   // Cao cấp 1 - ~6000 từ vựng
    N1    // Cao cấp 2 - ~10000+ từ vựng
}
